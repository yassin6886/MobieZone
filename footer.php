	var helpLink = document.querySelector("#helpLink");
	var contactCard = document.querySelector("#contactCard");
	var closeBtn = document.querySelector("#close");
	helpLink.addEventListener("click", function(event) {
		event.preventDefault();
		loadContactCard();
	});

	function loadContactCard() {
		var xhr = new XMLHttpRequest();
		xhr.onreadystatechange = function() {
			if (xhr.readyState === XMLHttpRequest.DONE) {
				if (xhr.status === 200) {
					contactCard.innerHTML = xhr.responseText;
					contactCard.style.display = "block";
				} else {
					console.error("Error: " + xhr.status);
				}
			}
		};
		xhr.open("GET", "contactCard.php", true);
		xhr.send();
	}

	closeBtn.addEventListener("click", function() {
		contactCard.style.display = "none";
	});

	window.addEventListener("click", function(event) {
		if (event.target == contactCard) {
			contactCard.style.display = "none";
		}
	});
</script>

